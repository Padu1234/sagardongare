package array;

import java.util.Arrays;

public class ArraySortSmart {
public static void main (String[]args) {
	
	int[] a = {12,34,56,87,98,3,7,98,0,90};
	
	Arrays.sort(a);
	
	
	for (int i=0; i<a.length; i++ ) //assending order by arrays sort
		System.out.print(a[i] +" ");
	 

	
}


}	


		
	
			
			
			
			
			
			
		
	


